package com.lock.peter.nfcopen;

import android.app.Fragment;
import android.os.Bundle;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.TextView;

public class UnlockDoorFragment extends Fragment {

    public static final String TAG = "UnlockDoorFragment";

    private static final String ARG_PARAM1 = "UNLOCKPARAM";

    private String unlockParam;

    public UnlockDoorFragment() {
    }

    public static UnlockDoorFragment newInstance( String param1 ) {
        UnlockDoorFragment fragment = new UnlockDoorFragment();
        Bundle args = new Bundle();
        args.putString(ARG_PARAM1, param1);
        fragment.setArguments(args);
        return fragment;
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        if (getArguments() != null) {
            unlockParam = getArguments().getString(ARG_PARAM1);
        }

    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        View v = inflater.inflate(R.layout.activity_unlock_door, container, false);
        TextView text = (TextView)v.findViewById(R.id.tbox);
        text.setText(unlockParam);
        return v;
    }
}